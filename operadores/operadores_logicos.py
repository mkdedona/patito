# AND

resultado = True and True     # devuelve True
resultado = False and True    # devuelve False
resultado = True and False    # devuelve False
resultado = False and False   # devuelve False

#OR

resultado = True or True    # devuelve True
resultado = False or True   # devuelve True
resultado = True or False   # devuelve True
resultado = False or False  # devuelve False

#NOT

resultado = not True  # devuelve False
resultado = not False # devuelve True